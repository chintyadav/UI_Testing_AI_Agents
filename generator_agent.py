def _camel(pascal: str) -> str:
    """Convert a PascalCase class name to a camelCase variable name.
    Example: "LoginPage" → "loginPage"
    """
    return pascal[0].lower() + pascal[1:] if pascal else pascal


def _get_test_value(method_name: str) -> str:
    """
    Return the appropriate test value based on the method name.
    - Methods containing 'email' or 'username' → use DEFAULT_EMAIL
    - Methods containing 'password' or 'pwd' → use DEFAULT_PASSWORD
    - All others → use generic 'test_value'
    """
    method_lower = method_name.lower()
    if "email" in method_lower or "username" in method_lower:
        return f"'{DEFAULT_EMAIL}'"
    elif "password" in method_lower or "pwd" in method_lower:
        return f"'{DEFAULT_PASSWORD}'"
    else:
        return "'test_value'"


def _strip_markdown_fences(code: str) -> str:
    """
    Remove ```javascript / ```js / ``` fences that the LLM might wrap its
    output in even when instructed not to.  Leaves the raw code content.
    """
    code = re.sub(r"^```(?:javascript|js)?\n?", "", code, flags=re.MULTILINE)
    code = re.sub(r"\n?```\s*$",                "",  code, flags=re.MULTILINE)
    return code.strip()


def _fix_strict_mode(code: str) -> str:
    """
    Post-process LLM-generated Page Object code before writing to disk.

    THE ROOT CAUSE we discovered:
        .first is a Locator property in Playwright >= 1.14.
        On ANY version mismatch, or when the LLM writes .first incorrectly,
        you get: TypeError: .first.fill is not a function

    THE PERMANENT FIX — never use .first or .last at all:
        .nth(0)  is a METHOD with parentheses, works on every Playwright version.
        .nth(-1) replaces .last.

    This function converts ALL .first/.last patterns to .nth() and
    adds .nth(0) before unguarded .click() and .fill() calls.
    """
    # ── Pass 1: .first.<action> → .nth(0).<action>  (property chain style)
    code = re.sub(
        r'\.first\.(click|fill|type|hover|check|uncheck|press|clear|selectOption)',
        r'.nth(0).\1', code
    )

    # ── Pass 2: .first().<action> → .nth(0).<action>  (old method-call style)
    code = re.sub(
        r'\.first\(\)\.(click|fill|type|hover|check|uncheck|press|clear|selectOption)',
        r'.nth(0).\1', code
    )

    # ── Pass 3: any remaining .first() → .nth(0)
    code = re.sub(r'\.first\(\)', '.nth(0)', code)

    # ── Pass 4: any remaining bare .first → .nth(0)
    code = re.sub(r'\.first\b', '.nth(0)', code)

    # ── Pass 5: .last variants → .nth(-1)
    code = re.sub(
        r'\.last\.(click|fill|type|hover|check|uncheck|press|clear|selectOption)',
        r'.nth(-1).\1', code
    )
    code = re.sub(r'\.last\(\)\.', '.nth(-1).', code)
    code = re.sub(r'\.last\b', '.nth(-1)', code)

    # ── Pass 6: line-by-line — add .nth(0) before bare .click()/.fill()
    fixed_lines = []
    for line in code.splitlines():
        stripped = line.strip()
        indent   = len(line) - len(line.lstrip())
        pad      = " " * indent

        if not stripped or stripped.startswith("//") or stripped.startswith("*"):
            fixed_lines.append(line)
            continue

        # Add .nth(0) before .click() when no scope guard present
        if ".click(" in line and "this.page." in line and "expect(" not in line:
            if ".nth(" not in line:
                line = line.replace(".click(", ".nth(0).click(")

        # Add .nth(0) before .fill() when no scope guard present
        if ".fill(" in line and "this.page." in line and "expect(" not in line:
            if ".nth(" not in line:
                line = line.replace(".fill(", ".nth(0).fill(")

        # ── Pass 7: ensure await on action lines (not inside expect)
        s = line.strip()
        if ("this.page." in s
                and "await" not in s
                and "expect(" not in s
                and not s.startswith("//")):
            line = pad + "await " + s

        fixed_lines.append(line)

    return "\n".join(fixed_lines)


def _validate_generated_js(code: str, file_name: str) -> list[str]:
    """
    Quick sanity check on generated JavaScript before writing to disk.
    Returns a list of error strings (empty = clean).

    Checks:
      - No .first() remaining (property called as function)
      - No .last()  remaining
      - import { expect } present
      - export default class present
      - No bare `page.` (without this.) in method bodies
    """
    issues = []
    # .first and .last in any form — use .nth(0) / .nth(-1) instead
    if re.search(r"\.first", code):
        issues.append(f"{file_name}: .first found — use .nth(0) instead (works on all Playwright versions)")
    if re.search(r"\.last", code):
        issues.append(f"{file_name}: .last found — use .nth(-1) instead")
    if "import { expect }" not in code:
        issues.append(f"{file_name}: missing import {{ expect }}")
    if "export default class" not in code:
        issues.append(f"{file_name}: missing export default class")
    # Bare page. in method body (not this.page, not in a comment)
    for line in code.splitlines():
        s = line.strip()
        if re.search(r'(?<![.\w])page\.\w+\(', s) and "this.page" not in s and not s.startswith("//"):
            issues.append(f"{file_name}: bare `page.` call (should be `this.page.`): {s[:80]}")
    return issues
    """
    Determine whether a method should accept a `value` parameter.
    Triggers if the method name starts with a fill/input verb OR the
    description contains fill/enter.
    """
    name_triggers = re.search(
        r"^(enter|fill|set|input|type)", method_name, re.IGNORECASE
    )
    desc_triggers = re.search(
        r"\b(fill|enter|type|input)\b", description or "", re.IGNORECASE
    )
    return bool(name_triggers or desc_triggers)


