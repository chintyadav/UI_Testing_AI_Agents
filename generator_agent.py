def _method_needs_param(method_name: str, description: str) -> bool:
    """
    Return True if this method should accept a `value` parameter.
    credential use email:harsh.yadav262002@gmail.com and password:Harsh@123
    Triggered when the method name starts with an input verb (enter/fill/set/
    input/type) OR the description explicitly mentions filling/entering a value.

    Examples:
        enterEmail()      → True   (name starts with "enter")
        fillSearchBox()   → True   (name starts with "fill")
        clickLogin()      → False
        verifyPage()      → False
    """
    name_triggers = re.search(
        r"^(enter|fill|set|input|type)", method_name, re.IGNORECASE
    )
    desc_triggers = re.search(
        r"\b(fill|enter|type|input)\b", description or "", re.IGNORECASE
    )
    return bool(name_triggers or desc_triggers)


def _generate_page_object(page_name: str, pg: dict) -> str:
    """
    Ask the LLM to write a Playwright Page Object class for a single page.

    The prompt provides:
      • The exact class name and file name
      • The exact import structure required
      • A METHOD CONTRACT for every method — type, locator, description
      • Absolute rules that prevent the most common LLM errors

    Returns the cleaned JavaScript code string (no markdown fences).
    """
    class_name = pg["class_name"]
    file_name  = pg["file_name"]
    methods    = pg["methods"]

    # Build method contracts (one per method, clearly formatted)
    contract_lines: list[str] = []
    for m in methods:
        has_param = _method_needs_param(m["method_name"], m.get("description", ""))
        param_sig = "(value)" if has_param else "()"
        contract_lines.append(
            f"  METHOD: async {m['method_name']}{param_sig}\n"
            f"    type        : {m['type']}\n"
            f"    locator     : {m['locator']}\n"
            f"    description : {m['description']}\n"
            f"    navigates_to: {m['navigates_to'] or 'none'}"
        )

    all_contracts = "\n\n".join(contract_lines)

    # Build failure context from previous test run (empty on first attempt)
    prior_failures = pg.get("_test_failures", [])
    failure_context = (
        "None — this is the first generation attempt."
        if not prior_failures
        else "\n".join(
            f"  - [{f['error_type']}] {f['error_msg'][:300]}"
            for f in prior_failures
        )
    )

    prompt = f"""You are a Playwright Page Object Model expert.
Generate a JavaScript Page Object file using ONLY the syntax shown in the
PLAYWRIGHT SYNTAX REFERENCE below.

━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
PLAYWRIGHT SYNTAX REFERENCE  (authoritative — follow exactly)
━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
{_SYNTAX_REFERENCE}

━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
FILE TO GENERATE
━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
File  : pages/{file_name}
Class : {class_name}

Required file structure:

import {{ expect }} from '@playwright/test';

export default class {class_name} {{
  constructor(page) {{
    this.page = page;
  }}
  // methods below
}}

━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
NON-NEGOTIABLE RULES
━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
R1. import {{ expect }} ONLY — NEVER import {{ test }}
R2. Every method → async
R3. Every Playwright action call (.click, .fill, .type, .hover) → preceded by await
R4. Use this.page everywhere — NEVER accept `page` as a method parameter
R5. Validation (TYPE=validation): await expect(this.page.<locator>).toBeVisible()
R6. Action     (TYPE=action):     await this.page.<locator>.click()
                                  await this.page.<locator>.fill(value)

R7. STRICT MODE — when a locator may match multiple elements:
    • ALWAYS use .nth(0) — it is a METHOD with parentheses, works on ALL Playwright versions:
        await this.page.getByPlaceholder("search").nth(0).fill(value);
        await this.page.getByRole("button", {{ name: "Add" }}).nth(0).click();
    • For a specific card scope:
        await this.page.locator(".card").nth(0).getByRole("button").click();
        await this.page.locator(".card", {{ hasText: "ADIDAS" }}).getByRole("button").click();

R8. NEVER use .first or .last — they cause TypeError on many Playwright versions.
    ALWAYS use .nth(0) for first element, .nth(-1) for last element.
R9. Use ONLY locators from METHOD CONTRACTS below — do not invent any
R10. NO_LOCATOR placeholder → write: // TODO: locator not captured

━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
METHOD CONTRACTS — implement each method exactly as specified
━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
{all_contracts}

━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
PREVIOUS TEST RUN FAILURES (fix these in the generated code)
━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
{failure_context}

━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
OUTPUT: JavaScript only. No markdown fences. Start with the import line."""

    raw = invoke_llm_with_retry(_llm, prompt)
    raw = _strip_markdown_fences(raw)
    fixed = _fix_strict_mode(raw)

    # Post-generation validation — catch anything _fix_strict_mode missed
    issues = _validate_generated_js(fixed, file_name)
    if issues:
        print(f"     ⚠️  Post-gen issues detected — applying emergency fixes:")
        for issue in issues:
            print(f"        🔴  {issue}")
        # Emergency nuclear pass: remove ALL () after .first and .last
        fixed = re.sub(r'\.(first|last)\(\)', r'.\1', fixed)
        # Re-validate
        remaining = _validate_generated_js(fixed, file_name)
        if remaining:
            print(f"     ❌  Could not auto-fix: {remaining}")
        else:
            print(f"     ✅  Emergency fixes applied successfully")

    return fixed


# ═════════════════════════════════════════════════════════════════════════════
#  SECTION 3 – SPEC FILE ASSEMBLER  (zero LLM — deterministic Python)
#
#  Why no LLM here?  The LLM cannot see the full list of generated Page Object
#  files and tends to:
#    - duplicate imports
#    - use wrong variable names
#    - invent method calls not in the plan
#
#  Python can assemble this perfectly from the parsed_pages dict.
# ═════════════════════════════════════════════════════════════════════════════

def _assemble_spec(parsed_pages: dict) -> str:
    """
    Assemble a Playwright test spec from the parsed plan.

    Structure of the generated spec:

        import { test, expect } from '@playwright/test';
        import LoginPage       from '../pages/LoginPage.js';
        import DashboardPage   from '../pages/DashboardPage.js';

        test('end-to-end test', async ({ page }) => {
          const loginPage     = new LoginPage(page);
          const dashboardPage = new DashboardPage(page);

          await page.goto('<BASE_URL>');

          // ── Login Page ──
          await loginPage.enterEmail('harsh.yadav262002@gmail.com');
          await loginPage.enterPassword('Harsh@123');
          await loginPage.clickLogin();
          // → navigated to: Dashboard Page

          // ── Dashboard Page ──
          await dashboardPage.verifyDashboardPage();
          await dashboardPage.clickProduct();
          ...
        });

    Returns the complete spec file content as a string.
    """
    unique_pages = list(parsed_pages.values())   # OrderedDict — visit order preserved

    # 1. Import block
    import_lines = ["import { test, expect } from '@playwright/test';"]
    for pg in unique_pages:
        import_lines.append(
            f"import {pg['class_name']} from '../pages/{pg['file_name']}';"
        )

    # 2. Instantiation block (inside test body)
    inst_lines: list[str] = []
    for pg in unique_pages:
        var_name = _camel(pg["class_name"])
        inst_lines.append(
            f"  const {var_name} = new {pg['class_name']}(page);"
        )

    # 3. Test body — walk pages in visit order, call each method once
    body_lines: list[str] = [
        f"  await page.goto('{BASE_URL}');",
    ]

    for pg in unique_pages:
        var_name = _camel(pg["class_name"])
        body_lines.append(f"\n  // ── {pg['page_name']} ──")

        for m in pg["methods"]:
            mname = m["method_name"]
            if _method_needs_param(mname, m.get("description", "")):
                test_value = _get_test_value(mname)
                body_lines.append(f"  await {var_name}.{mname}({test_value});")
            else:
                # .first is already added inside page object methods
                body_lines.append(f"  await {var_name}.{mname}();")

            if m["navigates_to"]:
                body_lines.append(f"  // → navigated to: {m['navigates_to']}")

    # 4. Assemble full file
    spec_parts = [
        "\n".join(import_lines),
        "\n",
        "test('end-to-end test', async ({ page }) => {",
        "\n".join(inst_lines),
        "\n".join(body_lines),
        "});",
    ]

    return "\n".join(spec_parts) + "\n"


